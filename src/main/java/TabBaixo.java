    public static void listarTab1(){
        DefaultTableModel modelo = (DefaultTableModel) tabBaixo.getModel();
        int i = 0;
        
        modelo.setRowCount(i);
        
        for(Baixo b1 : JanelaBaixo.getGerBaixo().getBdBaixo()){
            modelo.insertRow(i, new Object[]{b1.getCodigo(), b1.getTipo(), b1.getMarca(), 
                    b1.getModelo(), b1.getCaptador(), b1.getQtdCordas(), b1.getVenda1().getPreco()});
            i++;
        }
    }

